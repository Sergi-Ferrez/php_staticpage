    <h2>Niágara Falls</h2>
    <span>EE.UU. Y Canadá</span>
    <p>Situadas en el <strong>río Niágara</strong>, en la frontera entre y Canadá, se encuentran estas tres cataratas que
        embelesan al viajero con sus historias y leyendas. Para recorrerlas, el lado americano ofrece la
        oportunidad de embarcar en el Maid of the mist, el barco que recorre la parte inferior de los saltos
        ofreciendo a los turistas impresionantes vistas. Por su parte, en la parte canadiense, en Ontario,
        es Hornblower Niagara Cruises el que se encarga de hacer el mismo recorrido. Esta parte de las
        cataratas del Niágara es la que ofrece las mejores panorámicas, aunque también es la que está más
        edificada, pues se ha convertido en un destino muy popular en las lunas de miel.</p>



    <h2>Iguazú</h2>
    <span>Argentina y Brasil</span>
    <p>Las <strong>cataratas de Iguazú</strong> son uno de los principales reclamos turísticos de Misiones,
        al norte de, país donde se concentran la mayoría de estos saltos de
        agua que hacen frontera con Paraná, en. La cascada más afamada es <strong>la Garganta del diablo,
        que con 80 metros es el más alta</strong> de las más de 270. Para poder recorrerlas todas el
        parque natural donde se encuentra cuenta con un tren ecológico que acerca a los viajeros hasta las
        cascadas. También hay varias embarcaciones para verlas desde cerca y pasarelas de madera que se
        adentran en la selva para poder apreciar la <strong>riqueza de la flora y la fauna del
        lugar</strong>.</p>



    <h2>Cataratas Victoria</h2>
    <span>Zambia y Zimbabwe</span>
    <p><strong>Llamadas así en honor a la reina Victoria de Inglaterra</strong> por el descubridor David
        Livingstone, estas impresionantes cataratas son uno de los regalos que la naturaleza le ha otorgado
        a la África austral. Los locales, no obstante, las conocen como Mosi-oa-Tunya, que significa el agua
        que truena. Situadas entre la frontera de Zambia con Zimbabwe</a>, los saltos pueden contemplarse desde un
        antiguo puente
        de acero construido en 1905. Desde allí arriba se obtienen las <strong>mejores vistas de estos
        saltos de más de 100 metros de altura</strong> que están declarados patrimonio de la humanidad.
    </p>



    <h2>Salto del Ángel</h2>
    <span>Venezuela</span>
    <p>Con una altura de 979 metros, <strong>el salto del Ángel es la cascada más alta del mundo</strong>.
        Situada en el Parque Nacional de Canaima, esta maravilla natural y el entorno que la rodea han
        servido de inspiración para algunas películas como <em>Avatar</em> o <em>Up</em>. <strong>Paradise
        Falls alude a este rincón venezolano</strong>. Patrimonio de la humanidad desde 1994, esta es
        una de las mayores atracciones de Venezuela, aunque su acceso es bastante más complicado respecto a
        otros saltos, pues la zona está algo aislada debido al espesor de la selva donde se encuentra.</p>



    <h2>Plitvice</h2>
    <span>Croacia</span>
    <p>Conocido por sus numerosas cascadas y por sus lagos de azul celeste, <strong>el Parque Nacional de
        Plitvice</strong> es una de las maravillas naturales de. En total cuenta con 92 cataratas y cascadas que se
        funden con sus espesos bosques y los 16 lagos formados por el agua de los saltos. <strong>Declarado
        patrimonio de la humanidad en 1979</strong>, otro de los atractivos de Plitvice es la fauna que
        habita en él. Entre los animales que se pueden ver están el oso pardo, el lobo, el urogallo, linces
        y águilas, además de otras 120 especies de aves.</p>


    <h2>Cascada nevada</h2>
    <span>Islandia</span>
    <p>La <strong>cascada de Gullfoss es una de las principales atracciones naturales de Islandia</strong>.
        Se encuentra dentro del Circuito dorado que recorre el sur del país, en el cañón del río Hvitá. Su
        principal particularidad es que cuenta con <strong>tres grandes escalones y dos saltos de 32 metros
        de altura</strong>. Aunque en el siglo XX el Gobierno de Islandia se planteó vender la cascada
        para obtener energía hidráulica, actualmente este lugar está protegido y es una de las zonas más
        turísticas del país. Aunque no es la única catarata, Gullfoss es la más conocida por su proximidad a
        Reikiavik.</p>



    <h2>Sutherland</h2>
    <span>Nueva Zelanda</span>
    <p>Situada en <strong>Milford Sound</strong>, el fiordo del sur de, la cascada de Sutherland es conocida por ser una de
        las más altas del país junto con Browne. Cuenta con una <strong>caída de 580 metros dividida en tres
        tramos</strong>. Declarada patrimonio de la humanidad, Sutherland es muy visitada por
        encontrarse en el Parque Nacional de Fiorland, uno de los más grandes del país cuya principal
        característica es que se compone de diferentes fiordos, valles, lagos e islas. Su vida salvaje
        incluye delfines, focas, kiwis y ciervos.</p>



    <h2>Horsetail Falls</h2>
    <span>Estados Unidos</span>
    <p>La <strong>cascada de cola de caballo</strong>, también conocida como del fuego, <strong>se encuentra
        en el Parque Nacional de Yosemite, California</strong>. Su cauce proviene del deshielo de la
        nieve que se forma en la parte superior de la montaña, creando así una caída de 500 metros.
        Dependiendo de la época que la visitemos, su cauce será mayor o menor. Sin embargo, hay una época
        bastante especial para verla. En febrero, los últimos rayos del sol inciden en ella creando un
        efecto óptico que hace que su agua adquiera tonos anaranjados o rojizos y adquiera la apariencia de
        un río de lava.</p>



    <h2>Cataratas de sangre</h2>
    <span>Antártida</span>
    <p>Localizado en la parte frontal lengua del glaciar Taylor, en los Valles Secos de McMurdo, al este de
        la Antártida, esta cascada llama la atención desde tiempos inmemoriales debido al <strong>color de
        su agua que se asemeja a la sangre</strong>. En un principio se creyó que este tono rojizo venía
        de unas algas marinas que allí se encontraba. Sin embargo, la explicación científica es que este
        fenómeno se produce debido al óxido de hierro generado en la zona cada vez que mana agua salada.</p>



    <h2>Cataratas Jog</h2>
    <span>India</span>
    <p>Esta maravilla de 200 metros de altura se caracteriza por tratarse de <strong>cataratas
        aéreas</strong>. Esto quiere decir que no discurren sobre las rocas, sino que en la temporada de
        monzones son capaces de caer libremente formando un gran estruendo en el río Sharavathi, en el
        estado indio de Karnataka. Esta es la mejor época para visitarlas, ya que durante el verano debido a
        su cercanía a la presa de Linganmakki el caudal es poco abundante y las <strong>cataratas de
        Jog</strong> quedan prácticamente irreconocibles.</p>
